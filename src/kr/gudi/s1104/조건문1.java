package kr.gudi.s1104;

public class 조건문1 {

	public static void main(String[] args) {
		int a = 10;
		int b = 2;
		int c = 5;
		
		if((a / b) > c) {
			System.out.println("결과값이 c보다 크다");
		} 
		
		if((a / b) < c) {
			System.out.println("결과값이 c보다 작다");
		} 
		
		if((a / b) == c) {
			System.out.println("두개의 값은 같다");
		} 
		
	}

}
